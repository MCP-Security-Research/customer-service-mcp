"""Provides answers to common auto loan questions."""

def get_auto_loan_application_status():
	"""Returns information on how to find auto loan application status."""
	return (
		"You can check your auto loan application status by logging into your online banking account, "
		"navigating to the 'Loans' section, and selecting 'Application Status.' If you need further assistance, "
		"contact our customer service at 1-800-000-0000."
	)

def schedule_auto_loan_payment():
	"""Returns instructions on how to schedule auto loan payments."""
	return (
		"To schedule a payment for your auto loan, log in to your online or mobile banking, go to the 'Payments' section, "
		"select your auto loan account, and choose 'Schedule Payment.' You can set up one-time or recurring payments."
	)

def auto_loan_automated_recurring_payments():
	"""Explains the difference between automated and recurring payments."""
	return (
		"An automated payment is a payment that is automatically withdrawn from your account on a set date, "
		"often set up through your lender. A recurring payment is a payment you schedule to repeat at regular intervals, "
		"which you can manage or cancel through your online banking."
	)

def insufficient_funds_stop_auto_loan_automated_payment():
	"""Provides guidance on stopping an automated payment if there are insufficient funds."""
	return (
		"If you do not have enough funds for an upcoming automated payment, log in to your online banking and cancel or pause the payment, "
		"or contact customer service as soon as possible to avoid fees. You may also transfer funds into your account before the payment date."
	)
